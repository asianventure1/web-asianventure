                <p>Few countries have changed so much over such a short time as
                    Vietnam. Less than forty years since the savagery and slaughter of the
                    American War, this resilient nation is buoyant with hope. It is a
                    country on the move: access is now easier than ever, roads are being
                    upgraded, hotels are springing up and Vietnam’s raucous entrepreneurial
                    spirit is once again alive and well as the old-style Communist system
                    gives way to a socialist market economy. As the number of tourists
                    visiting the country soars, their talk is not of bomb craters and army
                    ordnance but of shimmering paddy fields and sugar-white beaches,
                    full-tilt cities and venerable pagodas; Vietnam is a veritable phoenix
                    arisen from the ashes.</p>
                <p>The speed with which Vietnam’s population has been able to put the
                    bitter events of its recent past behind it, and focus its gaze so
                    steadfastly on the future, often surprises visitors expecting to
                    encounter shell-shocked resentment of the West. It wasn’t always like
                    this, however. The reunification of North and South Vietnam in 1975,
                    ending twenty years of bloody civil war, was followed by a decade or so
                    of hardline centralist economic rule from which only the shake-up of <strong>doi moi</strong> – Vietnam’s equivalent of <em>perestroika</em>
                    – beginning in 1986, could awaken the country. This signalled a
                    renaissance for Vietnam, and today a high fever of commerce grips the
                    nation: from the flash new shopping malls and designer boutiques to the
                    hustle and bustle of street markets and the booming cross-border trade
                    with China. From a tourist’s point of view, this is a great time to
                    visit – not only to soak up the intoxicating sense of vitality and
                    optimism, but also the chance to witness a country in profound flux.
                    Inevitably, that’s not the whole story. <em>Doi moi</em> is an economic
                    policy, not a magic spell, and life, for much of the population, remains
                    hard. Indeed, the move towards a market economy has predictably
                    polarized the gap between rich and poor. Average monthly incomes for
                    city-dwellers are around US$100, while in the poorest provinces workers
                    may scrape by on as little as US$30 a month – a difference that amply
                    illustrates the growing gulf between urban and rural Vietnam.</p>
                <p>There is an equally marked difference between <strong>north and south</strong>,
                    a deep psychological divide that was around long before the American
                    War, and is engrained in Vietnamese culture. Northerners are considered
                    reticent, thrifty, law-abiding and lacking the dynamism and
                    entrepreneurial know-how of their more worldly wise southern
                    compatriots. Not surprisingly, this is mirrored in the broader economy:
                    the south is Vietnam’s growth engine, it boasts lower unemployment and
                    higher average wages, and the increasingly glitzy Ho Chi Minh City looks
                    more to Bangkok and Singapore than Hanoi.</p>
                <p>Many visitors find a vast number of places to visit that intrigue and
                    excite them in Hanoi, Ho Chi Minh City and the other major centres; but
                    despite the cities’ allure, it’s the country’s striking <strong>landscape</strong>
                    that most impresses. Vietnam occupies a narrow strip of land that hugs
                    the eastern borders of Cambodia and Laos, hemmed in by rugged mountains
                    to the west, and by the South China Sea – or the East Sea, as the
                    Vietnamese call it. To the north and south of its narrow waist, it
                    fantails out into the splendid deltas of the Red River and the Mekong,
                    and it’s in these regions that you’ll encounter the paddy fields,
                    dragonflies, buffaloes and conical-hatted farmers that constitute the
                    classic image of Vietnam.</p>
                <p>In stark contrast to the pancake-flat rice land of the deltas, Ha Long Bay’s labyrinthine network of <strong>limestone outcrops</strong>
                    loom dramatically out of the Gulf of Tonkin – a magical spectacle in
                    the early morning mist. Any trip to the remote upland regions of central
                    and northern Vietnam is likely to focus upon the <strong>ethnic minorities</strong>
                    who reside there. Elaborate tribal costumes, age-old customs and
                    communal longhouses await those visitors game enough to trek into the
                    sticks. As for <strong>wildlife</strong>, the discovery in recent years
                    of several previously unknown species of plants, birds and animals
                    speaks volumes for the wealth of Vietnam’s biodiversity and makes the
                    improving access to the country’s <strong>national parks</strong> all the more gratifying.</p>

